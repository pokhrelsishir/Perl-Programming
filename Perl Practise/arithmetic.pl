#!/usr/bin/perl

$x = 111;
$y = 11;

$res = $x + $y;
print "Result of Addition: $res\n";

$res = $x - $y;
print "Result of Substraction: $res\n";

$res = $x * $y;
print "Result of Multiplication: $res\n";

$res = $x / $y;
print "Result of Division: $res\n";

$res = $x % $y;
print "Result of Remainder Operation: $res\n";

$res = $x ** $y;
print "Result of Power Operation: $res\n";
